             <h1>Picasso</h1>
                        <h3>Pablo Picasso (1881–1973) was one of the most inventive and influential talents ever to explore the medium of print.</h3>

                        <p>  In addition to his many paintings, sculptures and drawings, the Málaga-born artist made over 2,400 prints during his career. From his early years as an artist in Paris to his old age in the South of France, printmaking offered Picasso the opportunity to develop ideas, tell stories and embark on new creative adventures.
                            
                            This exhibition offers insights into Picasso's life through his art, including his complex relationships with women and his partnerships with printers, publishers and other artists. Spanning his entire career, it follows his engagement with different printmaking techniques and his changing approaches to making art.
                            
                            The British Museum holds by far the UK's largest and most representative collection of Picasso's prints. The exhibition features prints made in the early 1900s, before and after Picasso's breakthrough painting Les Demoiselles d'Avignon (1907), which paved the way for Cubism. It also includes a group of prints from the Vollard Suite, a series of 100 etchings made in the 1930s that shows the influence of classical art on Picasso's work and reflects his tumultuous personal life.
                            
                            Picasso's focus on lithography and then linocut printing following the Second World War is explored, as is his move to the South of France, marking a change of lifestyle that is evident in his work. Thematic threads include the circus, the bullfight, love and sex, and Picasso's interactions on paper with artists from the past. The exhibition ends with a selection from the 347 Suite, named after the number of prints in the series, which Picasso completed aged 86 in 1968, in a remarkable burst of late creativity.</p>
